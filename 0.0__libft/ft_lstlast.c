#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	if (lst->next == NULL)
		return (lst);
		//recursively call ft_lstlast until it reaches the last element
	return (ft_lstlast(lst->next));
}
